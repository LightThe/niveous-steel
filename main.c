void shift_word(uint8_t word[], int right, int len){
    uint8_t temp;
    if (right == 1){
        temp = word[len-1];
        for (int i = len-1; i > 0; i--){
            word[i] = word[i-1];    
        }
        word[0] = temp;
    }
    else{
        for (int i = 1; i < len; i++){
            temp = word[i];
            word[i] = word[(i+1)%len];
            word[(i+1)%len] = temp;
        }
    }    
}

void sub_bytes(uint8_t state[], int inv, int size){
    for (int i = 0; i < size; i++){
        if (inv!=1){
            state[i] = g_aes_sbox[state[i]];
        }
        else{
            state[i] = g_inv_sbox[state[i]];
        }
    }
    
}

